    private void analizarLexico() throws IOException
    {
        int cont =1;
        
        String expr = (String) txt_Archivo.getText();
        Lexer lexer = new Lexer(new StringReader(expr));
        String resultado = "<Linea " + cont + ">" + "\t\tSimbolo\n";
        
        while(true)
        {
            Tokens token = lexer.yylex();
            if(token==null)
            {
                txtLexico.setText(resultado);
                return;
            }
            
            switch(token)
            {
                case Int:
                    resultado += "<Tipo Entero>\t" + lexer.lexeme + "\n";
                    break;
                case Char:
                    resultado += "<Tipo Caracter>\t" + lexer.lexeme + "\n";
                    break;
                case Float:
                    resultado += "<Tipo Flotante>\t" + lexer.lexeme + "\n";
                    break;
                case Double:
                    resultado += "<Tipo Double>\t" + lexer.lexeme + "\n";
                    break;
                case Cadena:
                case Sin_Regreso:
                    resultado += "<Palabra Reservada>\t" + lexer.lexeme + "\n";
                    break;
                case Identificador:
                    resultado += "<Identificador>\t\t" + lexer.lexeme + "\n";
                    break;
                case Numero:
                    resultado += "<Numero>\t" + lexer.lexeme + "\n";
                    break;
                case ERROR:
                    resultado += "<No Definido>\t" + lexer.lexeme + "\n";
                    break;
                case Parentesis_AP:
                    resultado += "<Parentesis Abierto>\t" + lexer.lexeme + "\n";
                    break;
                case Parentesis_CR:
                    resultado += "<Parentesis Cerrado>\t" + lexer.lexeme + "\n";
                    break;
                case Llave_AP:
                    resultado += "<Llave Abierta>\t\t" + lexer.lexeme + "\n";
                    break;
                case Llave_CR:
                    resultado += "<Llave Cerrada>\t" + lexer.lexeme + "\n";
                    break;               
            }
            
        }
        
    }

