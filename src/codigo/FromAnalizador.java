    private void btnAnalizarSinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarSinActionPerformed
        // TODO add your handling code here:
        
        String ST = txt_Archivo.getText();
        Sintax s = new Sintax(new codigo.LexerCup(new StringReader(ST)));
        
        try {
            s.parse();
            txtSintantico.setText("Cadena Validad");
            txtSintantico.setForeground(new Color(25,111,61));
        } catch (Exception ex) {
            
            Symbol sym = s.getS();
            txtSintantico.setText("Cadena Invalida. Linea: " + (sym.right + 1) + "Columna: " + (sym.left + 1) + ", Texto: \"" + sym.value + "\"");
            txtSintantico.setForeground(Color.RED);
        }
        
    }//GEN-LAST:event_btnAnalizarSinActionPerformed

